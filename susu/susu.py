import tkinter as tk
import datetime

class SusuPraiser(object):
    def __init__(self, truth_list):
        self.truth_list = truth_list
        self.truth_cnt = len(self.truth_list)
        self.window = tk.Tk()
        self.frame = None
        self.truth = None
        self.num = 99

    def config(self):
        self.window.geometry('400x200')
        self.window.resizable(False, False)
        self.window.title('susu praiser')
        self.frame = tk.Frame(self.window, width=400, height=200, bg='lightblue')
        self.frame.place(x=0, y=0)

    def run(self):
        self.truth = tk.Text(self.frame, width=60, height=1)
        self.truth.place(x=0, y=70)
        self.window.after(1000, self.refresh)
        self.window.mainloop()

    def refresh(self):
        self.truth.delete(0.0, tk.END)
        cur_t = datetime.datetime.now()
        (y, m, d, h, mi, s) = (cur_t.year, cur_t.month, cur_t.day, cur_t.hour, cur_t.minute, cur_t.second)
        pos = (h * 20 + m * 20 + s * 71) % self.truth_cnt
        the_truth = "      %d-%02d-%02d %02d:%02d:%02d 此时此刻, 苏苏是%s的" % (y, m, d, h, mi, s, self.truth_list[pos])
        self.truth.insert(tk.INSERT, the_truth)
        self.window.update()
        self.window.after(1000, self.refresh)

